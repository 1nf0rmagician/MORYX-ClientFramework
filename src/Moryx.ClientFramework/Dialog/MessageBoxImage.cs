namespace Moryx.ClientFramework.Dialog
{
    /// <summary>
    /// Enum for possible message box image types which can be 
    /// displayed in the box
    /// </summary>
    public enum MessageBoxImage
    {
        /// <summary>
        /// None image will be displayed
        /// </summary>
        None,

        /// <summary>
        /// Okay image will be displayed
        /// </summary>
        Ok,

        /// <summary>
        /// Error image will be displayed
        /// </summary>
        Error,

        /// <summary>
        /// ErrorHand image will be displayed
        /// </summary>
        ErrorHand,

        /// <summary>
        /// Attention image will be displayed
        /// </summary>
        Attention,

        /// <summary>
        /// Question image will be displayed
        /// </summary>
        Question,

        /// <summary>
        /// Exclamation image will be displayed
        /// </summary>
        Exclamation
    }
}
